        for ( ArgumentNode child : schemeLabel.getParent().getChildren() )
        {
            if ( child instanceof CQConnectionNode )
            {
                mainConnector = ( CQConnectionNode ) child;
            }
        }

        //get nexus
        PremiseNexusNode nexus  = ( PremiseNexusNode ) findNextNexus( mainConnector );

